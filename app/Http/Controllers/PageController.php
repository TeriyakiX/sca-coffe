    public function index()
    {
        $pages = $this->pages();

        $items = collect($pages)->map(function ($page, $slug) {
            return [
                'slug'  => $slug,
                'title' => $page['title'],
            ];
        })->values();

        return response()->json($items);
    }

    public function show($slug)
    {
        $pages = $this->pages();

        $page = $pages[$slug] ?? [
            'title'   => 'Страница не найдена',
            'content' => '<p>Информация отсутствует.</p>',
        ];

        return response()->json($page);
    }
